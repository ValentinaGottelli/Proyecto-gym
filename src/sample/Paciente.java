package sample;

public class Paciente {
    public String nombre;
    public String apellido;
    public String sexo;
    public int edad;
    public String disciplina;

    public Paciente(String nombre,String apellido,String sexo,int edad,String disciplina){
        this.nombre=nombre;
        this.apellido=apellido;
        this.sexo=sexo;
        this.edad=edad;
        this.disciplina=disciplina;
    }
}
